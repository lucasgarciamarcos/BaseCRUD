    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        DefaultTableModel val = (DefaultTableModel) tabelaField.getModel();

        val.setRowCount(0);

        try {
            Vector view = control.listar();

            for (int i = 0; i < view.size(); i++) {
                
                Vector vec = new Vector();
                
                vec.addElement(String.valueOf(((Cliente)(view.elementAt(i))).getCodigo()));
                vec.addElement(((Cliente)(view.elementAt(i))).getNome());
                vec.addElement(((Cliente)(view.elementAt(i))).getEmail());
                vec.addElement(((Cliente)(view.elementAt(i))).getTelefone());
                vec.addElement(((Cliente)(view.elementAt(i))).getData());

                val.addRow(vec);
            }
        } catch (Exception ex) {
            Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton4ActionPerformed

